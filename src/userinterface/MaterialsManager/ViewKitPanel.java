    void showData(){
        
        txtKitLabel.setText(kit.getKitLabel());
        txtKitBarcode.setText(kit.getKitBarcode());
        txtKitItems.setText(kit.getKitItems());
        txtLocation.setText(kit.getKitLocation());
        expiryDateChooser.setDate(kit.getKexpiryDate());
        statusComboBox.setSelectedItem(kit.getKitStatus());
        
    }
    
    void updateStatus(){
        
        txtKitLabel.setEnabled(false);
        txtKitBarcode.setEnabled(false);
        txtKitItems.setEnabled(false);
        txtLocation.setEnabled(false);
        expiryDateChooser.setEnabled(false);
    }

