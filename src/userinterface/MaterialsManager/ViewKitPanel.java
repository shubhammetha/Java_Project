    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
                if(txtKitLabel.getText().isEmpty() || txtKitBarcode.getText().isEmpty() || txtKitItems.getText().isEmpty() || txtLocation.getText().isEmpty()
                || expiryDateChooser.getDate() == null)
        {
            JOptionPane.showMessageDialog(null,"Enter all the values!");
        }
        else{
        kit.setKitLabel(txtKitLabel.getText());
        kit.setKitBarcode(txtKitBarcode.getText());
        kit.setKitItems(txtKitItems.getText());
        kit.setKexpiryDate(expiryDateChooser.getDate());
        kit.setKitLocation(txtLocation.getText());
        kit.setKitStatus((String) statusComboBox.getSelectedItem());
        
        JOptionPane.showMessageDialog(null,"Kit Added Successfully!");
        txtKitLabel.setText("");
        txtKitBarcode.setText("");
        txtKitItems.setText("");
        txtLocation.setText("");
        expiryDateChooser.setDate(null);
        } 
    }//GEN-LAST:event_btnUpdateActionPerformed


